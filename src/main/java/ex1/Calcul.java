package ex1;


public class Calcul {

	/**
	 * 2 entiers en arguments
	 * Additionner  
	 * @author Aurelie-B
	 */
	public int addition (int a, int b) {
		return a+b;
	}
	
	/**
	 * 2 entiers en arguments
	 * Soustraire 
	 * @author Aurelie-B
	 */
	public int soustraction (int a, int b) {
		return a-b;
	}
}
