package ex1;

/**
 * Représente des calculs
 * @author Aurelie-B
 */
public class Calcul {


	/**
	 * @param a
	 * @param b
	 * @return résultat d'une addition de 2 entiers
	 */
	public int addition (int a, int b) {
		return a+b;
	}
	
	/**
	 * @param a
	 * @param b
	 * @return résultat d'une soustraction de 2 entiers
	 */
	public int soustraction (int a, int b) {
		return a-b;
	}
}
